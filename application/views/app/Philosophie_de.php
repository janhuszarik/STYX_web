						<h4>Klimaneutrale Produktion</h4>
						<p>	Gleichberechtigt neben der Natürlichkeit der Produkte steht für uns deren ökologisch nachhaltige Produktion.
							Die Verwendung erneuerbarer Energien ist für uns dabei selbstverständlich. Eine werkseigene Hackschnitzelanlage
							in Verbindung mit planvoller Wiederaufforstung durch unsere regionalen Partner sowie die ausschließliche
							Verwendung von Ökostrom lassen uns klimaneutral produzieren.
						</p>

						<h4>Ökostrom von Naturkraft</h4>
						<p>	STYX bezieht ausschließlich grünen Strom, der aus erneuerbaren Energiequellen wie Wasser, Wind, Biomasse, Erdwärme oder Sonne erzeugt wird.
							Mit dem Umstieg auf Ökostrom leistet das Unternehmen einen wertvollen Beitrag zum Umweltschutz und zur Nutzung natürlicher Ressourcen.
							STYX  betreibt  seit  August  2013  eine  Photovoltaikanlage. Die Anlage umfasst Kollektoren auf einer Fläche von 280 m2, welche
							jährlich rund 54.000 kWh Strom produzieren. Die Kraft der Sonne wird optimal genutzt, denn bereits 30% des jährlich benötigten Strombedarfs produziert
							STYX mit der Energie der Sonne selbst. 2011 hat STYX ein Biomasse Heizwerk installiert, das mit Hackschnitzel betrieben wird. Die Hackschnitzel
							bezieht STYX selbstverständlich von Landwirten aus der Region. Die jährliche Einsparung durch das Biomasseheizwerk beträgt knapp 30.000 m3
							Gas und 58,5 Tonnen CO2 Emissionen. STYX produziert damit völlig CO2 neutral. Mit dieser Leistung ist STYX europaweit einzigartig in der Kosmetikbranche.
						</p>
						<h4>Maschinenpark</h4>
						<p>STYX produziert das gesamte Sortiment in drei verschiedenen Produktionshallen. Je nach Verpackungsart gibt es eigene Maschinen für die Tiegelabfüllung,
							die Tubenabfüllung und die Flaschenabfüllung. Gemischt werden alle Rohstoffe in einem der zwei Cremenmischer. Diese haben eine Kapazität von 100 kg
